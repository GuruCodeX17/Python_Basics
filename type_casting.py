# Type Casting in Python

print("===== Type Casting =====")

# String to Integer
age = int(input("Enter your age: "))

# String to Float
height = float(input("Enter your height in meters: "))

# Integer to Float
marks = 95
marks_float = float(marks)

# Float to Integer
pi = 3.14
pi_int = int(pi)

print("\n----- Output -----")
print("Age:", age)
print("Height:", height)
print("Marks (float):", marks_float)
print("Pi (integer):", pi_int)